<articleTitle :title="`戰後的基隆防空洞`" />


<articleText :text="`
國民政府來台之後，防空洞的興建其實並未停歇。根據 1950 年《基隆市防護組織規程》記載，日本時代遺留下來的防空洞共有 396 座。從議會討論紀錄中也能看出，這 396 座防空洞，遠不足以容納當時基隆的人口，並提到應陸續補足人民避難需要的數量。後續在《基隆市年鑑》中，曾於 1970 年與 1973 年分別出現兩次防空避難設備的統計資料。若以此推估，七○年代初期的基隆，防空洞數量幾乎達到高峰，約有將近七百座，散布於這座多雨的港都之中。<br><br>
翻閱文件，也有一些令人印象深刻的防空洞。根據《基隆市年鑑》記載，1968 年基隆市民防部為了執行「加強民間核子防護」的指令，在中正公園市議會左側山腰，興建了一座核子防護示範公用防空洞。該洞全長 55.52 公尺，高、寬各 2.5 公尺，可容納 276 人，以 30 公分厚的雙層鋼筋混凝土建造，不但結構堅固且抗力極強，出入口皆設有防核射線的特殊門，內部配備通風與照明設備。<br><br>
依照書中的描述，我也曾親自探訪這座位於基隆市議會旁的防空洞。從外觀可看到那扇帶著青綠色、厚重的鐵門靜靜嵌在山壁間，一側出口通往市議會旁的坡道，另一側則延伸至岐峰小徑。有機會也想，推開這綠色的鐵門，進去一探究竟。


