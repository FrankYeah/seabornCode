<articleTitle :title="`是山洞也是防空洞`" />


<articleText :text="`
基隆因地形關係，在二戰時期與戰後陸續建造了許多防空洞，不同於臺灣其他地區常見的磚造、水泥防空洞，或是丸龜型防空洞。基隆山多平地少，防空洞大多就地取材，直接開鑿山壁建造「山洞型防空洞」。臺灣其他縣市的防空洞多數都在都市發展中被拆除、填平或掩埋，然而基隆的防空洞，大多只是被鎖上鐵門，靜靜留在原地。<br><br>
因此，在基隆的山壁上常能看到那些被封住的洞口—像是在八堵車站外、南榮路上、西岸中山三路邊，也可能就藏在你念書的學校裡、每天經過的樓梯旁或是你家後面，但大部分的人們都視而不見。<br><br>
提到南榮路上的防空洞，其實在《許曹德回憶錄》(1995)中，也有提及：<br><br>
「每家每戶都必須準備一種預防敵機轟炸的棉製三角帽，帽沿垂肩。記得第一次聽到空襲警報演習，那種長嘯急鳴的警報聲，初聞，叫人驚怖恐慌。在學校也實行空襲演習，如何就地躲避，如何奔進各地都有的防空山洞。此時，瀧川地區馬路旁的沿山地帶，都挖有一個一個的防空壕，就是躲避敵機之用。」<br><br>
他提到瀧川地區馬路旁的防空洞，所謂瀧川地區，應指當時的瀧川町，約略位於現今仁愛區南方一帶，正是南榮路貫穿經過的區域。許曹德當時就讀的基隆市瀧川公學校，亦即今日的南榮國小。直到今天，從基隆市區一路經過南榮國小、南榮公墓，直到八堵車站沿線的山壁上，仍可看見數十個明顯的防空洞，只可惜鐵門深鎖，無法進一步探訪。


