<articleText :text="`
那幾年間我在基隆走訪了許多洞。從廟口夜市旁的許梓桑防空洞、文安里的百年防空洞、智慧里社區發展協會後方的防空洞，到高遠新村山腰間的防空洞群，還有好多好多。有時，我會在洞裡閉上眼想像，時間回到二戰，美軍空襲基隆港，砲火不斷。人們躲在防空洞裡，等待轟炸過去。如今的防空洞，潮濕、發霉、冰涼又黏膩，與日常生活已無法連結。裡頭大多是垃圾、廢棄物，或是恐懼、害怕、戰爭的殘影；彷彿現代社會不需要、不想要的東西，都被封存在一個個防空洞裡。<br><br>
這次回到基隆，再次走進整修完成的信二路防空洞，明亮的燈光映照著人來人往的遊客，大部分人都驚呼連連。或許未來，對基隆人而言，防空洞的印象將會是信二路——明亮、安全、奇異、非日常的空間。撫摸著潮濕的牆面，時光倒轉回最初來到這裡的時刻。那時沒有燈光、沒有指引，裸露的岩壁尚未整理。我們曾在洞裡討論、想像它的未來，然而時間飛快，把我帶到這個其實並不遙遠的未來，也就是現在。


