     台灣與琉球，這兩列在太平洋上相望的群島，看似被深邃的海水相隔，實則在歷史的長河中，共享著一道由產業、移動與生活交織而成的「群島記憶」。這並非教科書上的宏大敘事，而是隱藏在我們腳下土地、日常景觀中，等待被發掘的「微光」。<br><br>
