想像那條從金瓜石山上蜿蜒而下，直至水湳洞「十三層」選煉廠。黃金與礦砂在此匯聚，製錬後，再藉由鐵路送往基隆的「八尺門」碼頭。基隆，特別是和平島（八尺門），不僅是礦業的出口，更是連結海洋的樞紐。<br><br>
