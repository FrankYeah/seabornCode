而這次出海的任務，是前往台灣東部海域的黑潮，採集不同深度的海水。採水要做什麼呢？我這個門外漢疑惑地問。「我長期研究水中溫室氣體，除了海水，也會在河口採樣。上次來這個採點已經是十年前了。」老師回答。<br><br>

老師接著繼續補充：「這次採水共有8個點，最深會下到3000多米。海水向下流動，水質組成與密度會隨深度有所變化，流動速度也會越慢，3000米的海水大約是500-800年前的表面海水…」隨著越來越專業的講解，突然覺得科學彷彿是另一個多重宇宙。<br><br>
這次除了老師的研究室，也邀請了中山大學與高雄海科的研究室一同參與。同學們都曾參與採樣，資深研究生更有合作經驗，因此大家都很雀躍。啟航時氛圍輕鬆，讓人以為會是一趟海上郊遊，殊不知一場體力與意志力的試驗才要開始。
