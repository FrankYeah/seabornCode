歲月流轉，全球化浪潮改變了產業分工，基隆港的榮光逐漸褪色。當城市不再只為貨物服務，而是開始思考「人」的需求時，天橋的命運也迎來了轉折。過去作為「縫合」的橋，如今反倒成了阻礙都市景觀的龐然大物。<br><br>
隨著「市港再生」的口號響起，天橋一座座地消失，被視為城市進步的象徵。這不僅是都市地景的改變，更是一種集體空間經驗的質變。過去，天橋提供了一個公共的、略高於日常的視角，讓市民得以俯瞰港口的運作、火車的進出——那是整座城市賴以為生的巨大機械心臟。<br><br>
