今天，數位時代的基隆人，仍然與世界連結著，但方式已截然不同。他們坐在可能是由昔日的委託行或報關行改建的咖啡廳裡，透過筆電與手機遨遊在茫茫網海中。空氣中瀰漫的是咖啡豆的烘焙香，而非柴油與魚腥味，也是城市產業與生活形態改變的具體表現。不過，當世界變得更為快速與容易之際，人們卻反而懷念起過往那樣緩慢但卻富有溫度的連結形式，就像我們懷念那陳舊的天橋一樣。


