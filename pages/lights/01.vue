天橋的存在，始於港口的擴張。<br><br>
作為日治時期銜接世界的門戶，基隆港被賦予了帶動戰後經濟的重責大任。在「出口導向」的政策下，港區的現代化工程如火如荼地展開。為了讓貨物能最高效地從船上轉運至內陸，高架道路與平面道路重新佈局相接，本就狹窄的市區被切割更為破碎，像鋒利的刀，劃開了港區與市區的連結。<br><br>
港口越是繁忙，聯結車的引擎聲、壓過路面的轟隆聲也越是響亮，居住於此的人們，要親近海港，就變得越加困難。天橋，在這樣的時空背景下，作為一種「補償」與「縫合」的方案而被興建。它並非浪漫的觀景台，而是在城市發展邏輯下，為被切割的動線所做的必要補救。<br><br>
1960 年代經濟起飛，無數來自台灣各地的年輕人，提著簡便行囊，搭著客運或火車落腳基隆。天橋也成了他們從租屋處走向碼頭與工廠的日常路徑。橋下的火車鳴笛是上工的鬧鐘，橋上的海風吹拂著汗水與憧憬。不同族群的命運在此交會，他們共同的腳步聲，譜成了基隆黃金年代最真實的交響樂。


