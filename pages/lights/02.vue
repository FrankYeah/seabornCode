<articleTitle :title="`一本型錄，乘載海的記憶`" />


<articleText :text="`

除了食物外，老船員的生活中，也充滿外來的語彙。就像是老牌的「駿風企業」老闆藺全生他家裡總還保存一本幾十年的船上型錄。<br><br>
他笑著說：「這個カタログ（Katalogu），你看一下！」這裡的カタログ，可不是一般的商品冊，而是專門給船員在船上翻閱的型錄。船員們先在海上挑好想買的東西——藥品、生活用品、五金用品，什麼都有——下船後再找委託行購買拖貨。<br><br>
對許多不曾經歷過海上生活的人來說，看見那本厚重的型錄，必然是大開眼界的時刻。雖然現在的船員早已不再講起カタログ內的商品，但藺老闆依然珍藏著幾本，偶爾拿出來，讓人窺見當年船上貿易的風光。ㄒ
