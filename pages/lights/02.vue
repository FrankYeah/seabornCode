      <articleTitle :title="`耐磨的褲子，沉澱的城市記憶`" />


<articleText :text="`
在卡拉OK店周邊，不只有熱炒與小吃，還有一條曾經熙來攘往的街道，牛仔街。<br><br>
「牛仔褲」這個詞，和基隆港口的故事緊緊相連。最早，它誕生於美國西部，為礦工設計的耐磨工作褲，結實又實用。隨著美軍來到台灣，尤其是基隆這座美軍補給的重要港口，這樣的服裝與文化，也悄然登陸港城。船員與常把從海外帶回來的衣物與配件交給委託行販售，而這些漂洋過海的「牛仔褲」，很快成了潮流的代名詞。自來街一帶因此聚集了專賣牛仔服飾的店家。<br><br>
港口的顧客多半是跑船的工人，他們需要一條耐磨耐穿的褲子應付日夜的勞動。1950到80年代，是牛仔街最風光的年代。店鋪一家接著一家開張，門口掛滿藍靛色的褲子，陽光曬在布料上，顯得特別耀眼。對許多年輕人來說，穿上一條從港口帶回來的牛仔褲，不只是時尚，更是一種與國際接軌的自豪。<br><br>
基隆的外來語是街角的聲音、夜市的香氣、海上的書頁、霓虹下的歌聲，也是牛仔褲街的藍色回憶。每一個詞彙，都承載著這座港城的深刻情感——不管潮水如何退去，它們總會留在基隆人的心底。你下次到港邊走走時，可能還是會聽到不怎麼熟悉，但卻令人感到親切的外來語言，或許是印尼語，還是越南話，都是基隆這個移民大熔爐，展現它多元與包容的時刻。


