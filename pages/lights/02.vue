基隆港除了是許多貨物的上岸地外，也藏著多種海外來的聲音與詞彙。這些外來語，有些是從移民而來，有些則是透過食物的香氣傳遞，還有海上船員帶回的語彙，更有辛苦勞工的共同用詞。它們像潮水，隨著歷史起伏，拍打在這座港灣的岸邊。

