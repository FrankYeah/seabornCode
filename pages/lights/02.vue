基隆的外來語，隱藏在街頭巷尾。光是各式各樣的黑輪（おでん，oden），就能讓人看見這裡與日本文化留下的深刻聯繫。<br><br>
最為人熟知的黑輪，莫過於吉古拉。它的名字源自日文「ちくわ（chikuwa）」。遊客們經常能光顧港邊老闆的生意，觀賞他將雪白的魚漿，烤成如竹輪的形狀，吃起來又香又Ｑ。這些手工吉古拉不僅能直接品嚐，也常成為各式料理的元素，承載著基隆港城的生活記憶。<br><br>
若說吉古拉有工匠的細膩，另一攤「黑輪伯」則多了幾分溫情。他推著攤車賣超過四十年，燒賣與龍鳳腿原是老兵傳來的味道，被他融入基隆的海港生活，調出獨特香氣。許多人自年輕吃到白髮蒼蒼。雖然如今難再見到他的身影，但那熟悉的香味仍縈繞人心，提醒著大家：基隆的味道，不只是食物，更是時代與記憶交織的情感。

