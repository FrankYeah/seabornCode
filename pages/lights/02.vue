       <articleTitle :title="`讓卡拉OK為你的人生伴奏`" />


<articleText :text="`
關於港邊的記憶，夜晚盞盞亮起的霓虹燈也是經典。基隆港邊的卡拉OK店閃亮的吸引著故人的腳步。裡面有人正舉杯大笑，更多的人是盯著螢幕裡的字幕，大聲高歌一曲。<br><br>
「卡拉OK」這個詞來自日文。是日本人1970年代發明的播放伴奏的新方式，「空オケ」意指「空無一人」，「オケ」則是「オーケストラ（管弦樂團）」的縮寫。沒有樂團在場，卻能讓人盡情高歌。這份來自異國的創意，沒多久便漂洋過海，落地在基隆這座港城。<br><br>
之前認識的卡啦OK店老闆霞姐，總在店裡忙進忙出，端著啤酒、補小菜。她說，早年的夜晚更是熱鬧得不得了，工人們一下班就成群結隊地湧進來，先點上幾盤滷味，再輪流唱自己最拿手的老歌。有人唱到眼眶泛紅，也有人笑得合不攏嘴。<br><br>
雖然現在年輕人多半去裝潢華麗的KTV，但霞姐仍守著這裡。她說，對許多客人來說，這裡不只是唱歌的地方，更是心情的出口。

