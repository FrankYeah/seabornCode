<articleText :text="`
2018年「星濱山工作室」正式成立，透過當時火紅的FlyingV群眾募資、舉辦了第一次的港灣藝術節。嘗試與地方合作，打造了第一次的街區藝術節，作品分散在港區的閒置空間裡，有商店、有住家也有廢墟。憑著一份熱忱，我們在寧靜的港口創造了閃耀的光芒，那份閃耀到現在我還妥善地放在心裡，成為至今還在這座漁港努力的重要因子。<br><br>
