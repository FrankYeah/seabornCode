<articleText :text="`
現在的正濱漁港，在大家腦海中應該是一個很繽紛的地區，有著全台灣最成功的彩色屋案例，聚集了許多的風格店家，咖啡廳、餐廳、伴手禮店林立。<br><br>
每年除了我們星濱山舉辦的「永晝海濱美術館」，另外還有由政府主持的「潮藝術」同時在這個場域發生。創作、娛樂與商業好像從來沒有在正濱停止過，但其實我們剛來的時候不是這樣的呢！<br><br>
時間回到2017年的冬天，當時的正濱因為漁業沒落，彩色屋計畫尚未誕生，港邊多是白色藍色相間的房屋、空無一人的街道，留下的是一種純粹的安靜。我們被那份與海洋相依的寧靜給打動，因而提出了以「藝術共創」為核心的行動計畫。<br><br>
那時候的想法很單純，想透過「藝術」擾動這片漁港，藉由藝術家的創作過程，聽居民講自己的記憶，並且做出更深的回應。關於漁網職人的故事、關於港邊五金行的故事、關於船長的故事、關於漁港移民的故事，透過創作，讓這些片段慢慢編織出一張屬於正濱的感性地圖。<br><br>
很喜歡書豪在某篇報導上說的：「在藝術以外，我們所談的是一種『感染』。」透過創作讓正濱能容納更寬廣的族群，並且盡可能聆聽不同的聲音，他的那份真摯與浪漫，我真心佩服，並如此相信直到現在！


