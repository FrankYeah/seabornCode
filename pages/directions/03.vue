故事說到這裡，巴士剛翻過獅球嶺，悄悄地滑進了南榮國小。一場械鬥直接在眼前開打，我們彷彿回到了那個歷史現場，親眼見證漳州人與泉州人的鬥爭與死傷。<br><br>

