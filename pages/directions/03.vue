離開南榮國小後，車子接著就開往老大公廟，正巧趕上七月初一開龕門（俗稱的開鬼門），現場掛滿燈籠，好不熱鬧。我們走進現場感受，主委就站在樓梯口發送平安糕，前來祭拜的人潮一波接著一波，下午就要開門了，那股蓄勢待發的氣息，好像在宣告：鷄籠中元祭就要隆重登場！

