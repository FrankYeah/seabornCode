<articleTitle :title="`《親愛的房客》──在邊緣找到家的可能`" />

<articleText :text="`
鄭有傑導演於2020年的作品《親愛的房客》，與前面幾部電影一樣故事一樣發生在基隆港邊。故事發生於基隆一棟頂樓加蓋的老公寓。電影開場時，鏡頭帶著觀眾緩緩進入這座城市的濕潤與沉寂，一如主角林健一隱忍壓抑的生活狀態。公寓中住著作為「未亡人」的林健一，以及他不幸離世伴侶的母親秀玉與兒子悠宇，但卻也因為母親並不認可林健一與兒子王立維的感情，健一在片中像基隆的冬雨般，總是冷冽無聲，宛如這座城市本身——承擔著許多情感重量，卻常常被社會遺忘與忽視。<br><br>

也因為全片超過八成在基隆取景拍攝，導演將基隆的日常景象，如陰雨連綿的街道、擁擠濕滑的樓梯間──轉為主角心境的具象延伸。導演巧妙運用基隆的壓迫性與濕度，使角色的情緒在空間中自然流動。虎仔山的夜景、以及三連棟交錯的樓梯，也都被轉化為角色記憶與關係變化的重要節點。導演巧妙運用基隆的壓迫性與濕度，使角色的情緒在空間中自然流動。<br><br>

這部電影不只重新思考「家」的定義，也讓觀眾進入健一的世界──一個對社會毫無怨言地默默付出的角色。他每日奔波於醫院與學校之間，準備飯菜、幫忙洗澡、照顧作業，彷彿是這個家庭的支柱，但他的存在卻始終無法名正言順地被承認。這種若即若離的情感與身份困境，透過基隆的空間得到具體化。

   `" />


