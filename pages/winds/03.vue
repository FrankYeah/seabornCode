<articleTitle :title="`《聖保羅砲艇》──作為異地再現的基隆`" />

<articleText :text="`

1966年，改編自美國暢銷小說的《聖保羅砲艇》正在籌拍，背景設定在1920年代的中國，一艘美國砲艇在長江航行時，與要求廢除不平等條約的中國學生爆發衝突。由於電影拍攝時值冷戰期間，難以進入中國拍攝取景。<br><br>

導演勞勃．懷斯（Robert Wise），最終選定臺灣作為替代場景。他與劇組走訪北臺灣後，認為基隆的山海交界與殖民建築最具「異國風味」足以代表1920年代的「上海」，因而決定將基隆、淡水、艋舺等地打造成中國長江流域。<br><br>

儘管電影劇情與臺灣並無直接關聯，卻無意間留下了1960年代基隆港口的珍貴影像。我們得以看見歷經二戰轟炸修復使用，日治時期巴洛克風格的基隆火車站、基隆郵便局、林開郡洋樓、港邊一幢一幢的美麗洋行等建築樣貌，皆被封存於電影膠卷之中。

   `" />


