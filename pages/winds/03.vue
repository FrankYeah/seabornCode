<articleTitle :title="`《黑暗之光》與《千禧曼波》──城市與孤身之人的共鳴`" />

<articleText :text="`

全片幾乎在基隆拍攝，張作驥導演的《黑暗之光》。劇情圍繞著兩位主角康宜與阿平在成長過程中所面對的情感缺席、家庭破裂與社會排擠。他們生長與居住的基隆，不僅是物理上的居所，更是情緒的投射。從跨越港邊的人行天橋獅子橋再到基隆碼頭邊，導演運用基隆潮濕陰鬱的氣候，來深化角色的孤寂與絕望感。<br><br>

而侯孝賢導演的《千禧曼波》開頭就透過女主角 Vicky，在基隆中山陸橋的緩步獨行，橋上的藍色燈光，與林強的迷幻的電子配樂〈單純的人〉相互交織出一條告別破敗基隆，前往五光十色花花世界台北的道路。電影序幕就這麼在 Vicky 的獨白下展開。<br><br>

若說《聖保羅砲艇》讓戰後基隆成為他者的舞台，那麼二十世紀末的這兩部電影，就呈現出1990年代後，港口吞吐量衰退使城市發展逐漸停滯冷清，在導演之眼中成為孤獨情緒的容器，更映照出這座城市的沒落與寂寥。

   `" />


