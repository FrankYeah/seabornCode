<articleText :text="`
片中多次出現天台與陽台的場景，既象徵逃避現實的出口，也象徵無處安放的情感角落。天台上燃放仙女棒慶祝新年，窗邊窺看港邊的閃爍燈光，這些片段將基隆地景與角色間情感的表現，相互緊密連結在一起。而片中主角健一回憶起過往戀愛的快樂片段，也不時出現基隆光亮溫暖的片段穿插其中。鏡頭經常在室內與室外之間來回擺盪，窗邊的光線微弱、潮濕的牆壁發霉，這些細節與角色的孤寂與堅持彼此呼應。或許，是由相互承擔與照顧所構成的關係，才是真正能夠安放靈魂的地方。<br><br>

基隆在這部作品內，雖然同樣有著潮濕陰暗的樣貌，就像不被社會主流肯認的家庭形式，但導演也透過電影隱約告訴我們，基隆雖然邊緣但卻有著「愛」的可能。既是庇護也是邊緣，電影讓這座城市與邊緣角色之間產生深刻共鳴，也讓觀眾重新看見這座被忽視的城市之美與溫柔。

