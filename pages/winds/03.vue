<articleTitle :title="`電影為何選中基隆？`" />


<articleText :text="`

港邊的空氣混雜著海味與些許柴油味，總給人種潮濕多雨印象的基隆，對大多數臺灣人來說，這是一座距離台北不遠，卻相對有點邊緣的港口。可能也正因這種「有點城市味但又些許小鎮感」若有似無特質，「基隆」成為許多導演眼中的理想電影場景。<br><br>

而且三面環山腹地狹小的基隆，在不斷與海爭地的過程中，意外地造成建築層層堆疊，依山而建的屋舍錯落的市容。許多靠山陡峭的樓梯向上爬到盡頭，就能看見一望無際的大海，無需刻意取景與佈置，構出一幅山與海錯落的獨特畫面。<br><br>

自十七世紀就有不同人群移入的基隆，也保留了不同時期的建築與地貌，從考古遺址到現代高樓；從傳統市場到人行天橋；從碼頭倉庫再到造船廠廢墟。基隆各種樣貌的地景，也能替不同電影中角色的塑造更為加分。從《聖保羅砲艇》時代感濃厚的港邊洋樓，到《千禧曼波》如夢似幻的天橋，再到《親愛的房客》的破敗老公寓。這些基隆之「景」不只是電影中的一幕，更像是出演電影不可或缺的重要角色。

   `" />

