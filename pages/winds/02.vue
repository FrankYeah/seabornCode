看到貨櫃從岸邊被吊掛上船的那一刻，我想起這即是FOB(Free On Board) 船上交貨條款的轉移的當下！ FOB條款意指賣方在港口將貨物裝載到買方指定的船舶上，就完成交貨了；同時貨物的風險和責任也完成轉移。舉例來說，若剛好遇到颱風而造成損失，若貨物已經在商船上了，那就得買方負擔；反之，若貨物尚未裝載，是在港邊而受損，那就算賣方的責任了，在商港邊，這些紙上的術語，是真實的存在。
