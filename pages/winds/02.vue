<articleTitle :title="`貨物，一場有節奏的旅行`" />

<articleText :text="`

國際貿易是一個非常繁雜以及精細的分工，牽涉到跨國的規則。你可以想像成安排出國旅遊，會請旅行社訂機票、安排到機場的包車，到了機場後還得完成櫃檯報到、安檢和通關，才可以準備搭機出國。不同的是，海運處理的是貨物，所以需要許多的代理人協助貨物完成出口流程，貨物才可順利離開國門。一旦啟動進出口程序，所有相關的從業人便進入有節奏的物流之河。<br><br>

在跨國的貨物運輸裡，貨物承攬業者 (Forwarder) 就是旅行社的角色，安排訂船艙與貨櫃的空間與運送貨物到碼頭，報關行 (Customs broker) 是貨物的代理人，負責準備文件，協助貨物通過安檢與過海關。船公司則是航空公司，擔負運輸的角色。<br><br>

既然是貨物，裝載到貨櫃裡後，會鎖上貨櫃封條，防止貨物被偷竊或掉包，安全抵達才會開啟。每個封條有一組獨一無二的編碼，相當於代表這個個物品的身份證。貨物的旅行不是自己動身，而是靠一群「代理人」幫忙才能踏出每一步。

   `" />



<articleImg :marginBottom="'10px'" :img="`img/wind/v9_02_04.jpg`" />




<articleText :text="`

每當出貨時刻，我就進入一場緊湊且不可耽誤的旅行，如此才可確保跨國運輸的效能。出口商把貨物準備好後就向貨物承攬業者 (Forwarder) 諮詢船期與預訂船艙位置、安排好工廠到港口的陸運。貨物運送到港口後，再由報關行 (Customs broker) 清點數量、確認包裝，再依海關程序進行抽檢與報關，最後配合船公司完成裝運。<br><br>

跨國貿易與運輸有通則，所以今天我們可以如此有效率的安排運輸；但也因為各國還有各自眉眉角角的規定，所以才會有貿易壁壘。

   `" />

