眾多儀式裡，最受重視的莫過於新船啟航前的祈福儀式，無論東西方都特別講究。或許形式各有不同，但都是為了祈求航行順利與平安歸來。<br><br>

你是不是第一個想起的也是蘭嶼達悟族的下水祭儀？部落裡的男性穿著丁字褲（Kekjit no mehakay）進行拋船儀式，伴隨吶喊助陣的畫面令人印象深刻。而臺灣許多漁村除了進行傳統的科儀祭祀之外，也有「擲吉儀式」，從新船拋下麻糬給象徵魚群的眾人，有著滿載而歸的寓意。<br><br>

至於西方的下水儀式則是隨著時間不斷演變，從漁民習俗流傳到海軍，再到現代商船，一脈相承。最早源自古希臘對海神的祭祀，而後古巴比倫人殺牛獻祭，北歐海盜則是獻出奴隸的血。15世紀時，英國下水儀式改由國王使者以銀製高腳杯舉酒祝賀，並把象徵鮮血的葡萄酒灑在甲板上，最後將酒杯扔到船外。<br><br>

演變至今，現代東西方的新船多會以「擲瓶禮」作為啟航儀式，透過打碎酒瓶，祈願海上不再有船員遇難而拋出的漂流瓶。通常由象徵教母的女性將酒瓶砸向船頭，摔得愈碎愈好，代表新船將永遠航行平安。<br><br>

近年的新船儀式講求效率，命名擲瓶下水三項儀式經常聯合一併舉行。「擲瓶禮」也改成將酒瓶包於綵球之中，以紅繩連結船首及典禮台，等教母命名之後，以小斧頭斬斷繩子，繽紛的綵球隨之爆裂，酒瓶落下砸在船首，場面更加華麗有看頭。

