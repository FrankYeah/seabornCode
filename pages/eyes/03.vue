往來世界港口的商船是海上的移動城堡，船上聚集了不同國家的船員。不只一起工作，也長時間共同生活，船員們帶著母國的信仰與習俗在船上交匯，就像小小聯合國。<br><br>

緬甸船員們大多是虔誠的佛教徒，會在房間的窗邊擺上佛像小卡，早晚禮佛，偶爾也會把餐後的水果帶回房間供奉。而信奉伊斯蘭教的船員，即使上船工作也依然保持每天五次的禮拜，因此工作途中他們要是消失一小段時間，大概就是到某個角落進行禮拜了。<br><br>

而不少臺灣船員也會把傳統的道教祭祀帶上商船。在臺灣，不只漁民特別重視信仰與儀式，依海而生的港口碼頭也十分重視，而這一份虔誠也跟著上了商船，因此會聽到一些相似的民間習俗。<br><br>

例如吃魚不能翻面、遇見流水屍要善心協助打撈，以及屍體不能過船的龍骨等，都是漁民、商船與軍艦共同遵循的習俗。而臺灣的商船大多是由臺灣船長掌舵，若是船長重視，這些習俗禁忌就會比較明顯。像是萬海航運，每艘船上都會供奉媽祖神像，以保佑航行順利。

