     當我們提到與海洋相關的流行元素，條紋衫（Breton stripe）無疑是其中最為經典的代表之一。條紋衫的歷史可以從名字略為窺見，Breton 是指法國的沿岸的布列塔尼地區，1858 年法國要求駐守當地的海軍以上面印有 21 道藍白條紋的衣服為制服，萬一不小心墜海時，在遠方能更容易被看見。<br><br>

