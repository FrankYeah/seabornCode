在海洋帶來的眾多流行中，蘭姆（rum）酒的傳播與演變，可以說是最為經典的故事之一。蘭姆酒與海洋交織而來的傳奇，得從其原料甘蔗的傳播說起。<br><br>

目前的研究顯示，甘蔗可能原產於印度或東南亞等地，最早透過阿拉伯人傳到中東，再到了伊比利半島上的西班牙；而後又在16、17世紀的大航海時期，被殖民者帶到加勒比海的西印度群島上。這些甘蔗種植園在精煉糖的過程中產生的副產品(如糖蜜或甘蔗汁)，又進一步被發酵蒸餾，最終成了我們熟悉的蘭姆酒。<br><br>

同時，當時的人們也發現，富含維生素C的檸檬能夠抑止長時間航行所帶來的壞血病，於是船員會將檸檬汁加入蘭姆酒中，特殊的風味成為船上珍貴的保健飲品，迅速地在各國艦隊與海盜船上流行開來。蘭姆酒在某些時刻甚至可以當成貨幣使用。此後，蘭姆酒逐漸進入大眾市場，成為加勒比海與西方世界之間貿易的代表性商品之一。<br><br>

蘭姆酒的流行軌跡充分體現了海洋對於飲品文化的塑造力，從水手們的日常飲品變成了現代酒吧文化中的象徵。蘭姆酒也推動了調酒文化的發展，例如以鹽口杯、伏特加與葡萄柚汁混合的「Salty Dog」，這類海上風格的飲品逐漸風靡全球。

