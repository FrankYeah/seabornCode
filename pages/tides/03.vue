臺灣長期以來都過度依賴火力發電，導致碳排放量難以減少，需要增加更多綠色能源來減低碳排。其中，可以藉由自然能轉換可以儲存的「氫能源」，將有機會解決問題。<br><br>
 
而想要發展氫能源，就不得不提到它的特色功效。氫能源是可用於發電、交通運輸和工業用途的清潔能源。它是地球中最豐富的元素，儲存在水、天然氣和其他化合物中，當氫氣與氧氣反應時，會產生電能並釋放出水，因此是種零碳排放的能源，並且符合減碳淨零的趨勢。<br><br>
 
